public class IterationWhile
{
	// while, do while, for, for-each(for unleashed)
	public static void main(String[] args)
	{
		int i=1;// initialisation
		while(i<=5/*condition*/)
		{
			System.out.println("Hello");//prints in multiple lines
			System.out.print("Hello");// prints in a single line
			//loop text
			i++;// increment operation to match the loop
		}
	}
}